export default {
  name: "Menu",
  data() {
    return {};
  },
  methods: {
    /* ========================================================================================= */
    // ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    // Navbar Animation ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    navAnimation() {
      let tl = gsap.timeline({ defaults: { opacity: 0, ease: "power2.out" } });
      let listItems = document.querySelectorAll(".list");
      tl
        // .from("#main-nav", { duration: 0.5, ease: "back.inOut(1.7)", y: -500, scale: 0.7, autoAlpha: 0 }, "-=1")
        .from(
          "#main-nav",
          { duration: 2, ease: "sine", y: -500, scale: 0, autoAlpha: 0 },
          "-=1.5"
        )
        .from(
          ".menu-list2",
          { duration: 1.5, scale: 0, ease: "linear", y: -200, stagger: 0.3 },
          "-=1"
        )
        .from(
          ".menu-list1",
          { duration: 1.5, scale: 0, ease: "inear", x: -200, stagger: 0.2 },
          "-=1"
        )
        .from("#logo > g", { duration: 2, scale: 0, stagger: 0.8 }, "-=2")
        .from(
          "#logo #Circle",
          { duration: 1.5, opacity: 0.7, yoyo: true, repeat: -1 },
          "-=1"
        )
        .from(
          "#logo #A",
          { duration: 2.5, opacity: 0.7, yoyo: true, repeat: -1 },
          "-=1.5"
        )
        .from(
          "#logo #D",
          { duration: 2.5, opacity: 0.7, yoyo: true, repeat: -1 },
          "-=2"
        )
        .from(
          listItems,
          { duration: 1, opacity: 0.8, yoyo: true, repeat: 1, stagger: 0.2 },
          "-=3"
        )
        .from(
          "#durdona",
          {
            duration: 1.5,
            opacity: 0.2,
            yoyo: true,
            repeat: -1,
            stagger: 0.5,
            autoAlpha: 0
          },
          "-=2.5"
        )
        .from("svg #d2", { scale: 0, x: 150 }, "-=1")
        .from("svg #u", { scale: 0.1, y: 100 }, "-=1.2")
        .from("svg #rd", { scale: 0.2, x: 100 }, "-=1.3")
        .from("svg #ona", { scale: 0.2, y: 100 }, "-=1.5");
    }
  },
  mounted: function() {
    this.navAnimation();
    // ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    // Hamburger menu on click triggers animation ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
    const burger = document.querySelector(".burger");
    const navLinks = document.querySelector(".nav-links");
    const links = document.querySelectorAll(".nav-links li.list");

    let burgerTl = gsap.timeline();
    burger.addEventListener("click", function() {
      navLinks.classList.toggle("open");
      burgerTl
        .from(".burger", {
          duration: 0.5,
          scale: 0,
          opacity: 0,
          rotation: 360,
          transformOrigin: "50% 50%"
        })
        .from(
          links,
          { duration: 0.5, opacity: 0.9, yoyo: true, repeat: 1, stagger: 0.5 },
          "-=1"
        )
        .from(
          ".menu-list2",
          { duration: 0.5, scale: 0, ease: "linear", y: -100, stagger: 0.3 },
          "-=1"
        )
        .from(
          ".menu-list1",
          { duration: 0.5, scale: 0, ease: "inear", x: -100, stagger: 0.2 },
          "-=1"
        )
        .from(
          "#durdona",
          { duration: 1, yoyo: true, repeat: -1, stagger: 0.5 },
          "-=2.5"
        );
    });
  }
};
